main() {
  /**
   * For in
   * 
   * For in will loop all 
   * element in array
   * 
   * syntax:
   * for in (int i in array) {
   *  
   * }
   */

  var a = [1, 2, 3, 4, 5];

  /**
   * Print all element
   * in array a
   */
  for (int i in a) {
    print(i); //print each element one by one
  }
}
